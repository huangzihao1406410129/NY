package com.ks.ny.entity.vo;

public enum ResultCode {
    SUCCEED(1,"SUCCESS"),
    FAIL(0,"FAIL");
    private int code;
    private String msg;

    ResultCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
